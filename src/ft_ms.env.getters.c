#include <minishell.h>

void	update_env_tmp(char **new, t_env *e)
{
	ft_strsplit_free(&e->tmp_env);
	e->tmp_env = new;
}

char	***get_env(t_env *e)
{
	return (e->tmp_env ? &e->tmp_env : e->env);
}
